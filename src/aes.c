static const uint8_t rcon[11] = {
  0x8d, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36
};

static void expand_key(struct AES_ctx *ctx, const uint8_t *Key) {
	unsigned j, k;
	uint8_t tempa[4]; // Used for the column/row operations
	unsigned numrounds = getNumRounds(ctx->kt);
	unsigned keysize = getKeySize(ctx->kt);

	// First iteration is just a copy
	memcpy(ctx->RoundKey, Key, keysize*4);

	for (unsigned i = keysize, c = 1; i < 4 * (numrounds + 1); c++) {
		union {
			uint8_t   u8[4];
			uint32_t u32;
		} t = { .u32 = ctx->RoundKey[i-1] };
		// RotWord, use 32 bits since it's faster, endian agnostic macro
		// [a0,a1,a2,a3] -> [a1,a2,a3,a0]
		t.u32 = rotbyteleft(t.u32);
		// Subword
		for (unsigned p = 0; p < 4; p++)
			t.u8[p] = sbox[t.u8[p]];
		// Rcon step
		t.u8[0] ^= rcon[c];

		for (unsigned k = 0; k < 2; k++) {
			for (unsigned j = 0; j < keysize/2; j++, i++)
				t.u32 = ctx->RoundKey[i] = ctx->RoundKey[i - keysize] ^ t.u32;
			if (keysize == 8) {
				// AES256 subword
				for (unsigned p = 0; p < 4; p++)
					t.u8[p] = sbox[t.u8[p]];
			}
		}
	}
}

void AES_init_ctx_iv(struct AES_ctx *ctx, const uint8_t *key, AESKeyType kt, const uint8_t *iv) {
	ctx->kt = (uint8_t)kt;
	expand_key(ctx, key);
	if (iv)
		memcpy(ctx->iv, iv, AES_BLOCKLEN);
}

static void mix_key(uint32_t *state, const uint32_t *key) {
	// Use 32 bit integers for faster xoring
	for (unsigned i = 0; i < 4; i++)
		state[i] ^= key[i];
}

// sbox/rev-sbox lookup
static void sbytes(uint8_t *s) {
	for (unsigned i = 0; i < 16; i++)
		s[i] = sbox[s[i]];
}
inline static void isbytes(uint8_t *s) {
	for (unsigned i = 0; i < 16; i++)
		s[i] = rsbox[s[i]];
}

inline static void shiftrows(uint8_t s[4][4]) {
	uint8_t t1 = s[0][1];
	s[0][1] = s[1][1];
	s[1][1] = s[2][1];
	s[2][1] = s[3][1];
	s[3][1] = t1;

	uint8_t t2 = s[0][2];
	s[0][2] = s[2][2];
	s[2][2] = t2;
	uint8_t t3 = s[1][2];
	s[1][2] = s[3][2];
	s[3][2] = t3;

	uint8_t t4 = s[0][3];
	s[0][3] = s[3][3];
	s[3][3] = s[2][3];
	s[2][3] = s[1][3];
	s[1][3] = t4;
}

inline static void ishiftrows(uint8_t s[4][4]) {
	uint8_t t1 = s[3][1];
	s[3][1] = s[2][1];
	s[2][1] = s[1][1];
	s[1][1] = s[0][1];
	s[0][1] = t1;

	uint8_t t2 = s[0][2];
	s[0][2] = s[2][2];
	s[2][2] = t2;
	uint8_t t3 = s[1][2];
	s[1][2] = s[3][2];
	s[3][2] = t3;

	uint8_t t4 = s[0][3];
	s[0][3] = s[1][3];
	s[1][3] = s[2][3];
	s[2][3] = s[3][3];
	s[3][3] = t4;
}

static inline uint8_t xsqr(uint8_t x) {
	uint8_t r = x << 1;
	if (x >= 128)
		r ^= 0x1b;
	return r;
}

// Multiply is used to multiply numbers in the field GF(2^8)
// 9  001  0
// b  011  1
// d  101  2
// e  110  3

static uint8_t gfmul8(uint8_t x, uint8_t y) {
	uint8_t ret = 0;
	if (y < 3)
		ret ^= x;

	uint8_t x2 = xsqr(x);
	if (y & 1)
		ret ^= x2;

	uint8_t x4 = xsqr(x2);
	if (y >= 2)
		ret ^= x4;

	ret ^= xsqr(x4);
	return ret;
}

inline static void mixcols(uint8_t *s) {
	// Taken from wikipedia.
	// Rewrote this 4 times, each one gets completely different perf :|
	for (unsigned i = 0; i < 4; ++i) {
		uint8_t e0 = s[0];
		uint8_t e2 = s[2];
		uint8_t q0 = xsqr(s[0]);
		uint8_t q2 = xsqr(s[2]);

		uint8_t t1 = xsqr(s[1]) ^ s[3] ^ s[2];
		uint8_t t2 = xsqr(s[3]) ^ s[0] ^ s[1];
		s[0] = q0 ^ s[1] ^ t1;
		s[1] = q2 ^ e0 ^ t1;
		s[2] = q2 ^ s[3] ^ t2;
		s[3] = q0 ^ e2 ^ t2;

		s += 4;
	}
}

// See wikipedia for Rijndael_MixColumns
inline static void imixcols(uint8_t s[4][4]) {
	for (unsigned i = 0; i < 4; i++) {
		uint8_t t[4] = {s[i][0], s[i][1], s[i][2], s[i][3]};

		// 9bde -> encode -> 0123
		s[i][0] = gfmul8(t[0], 0x3) ^ gfmul8(t[1], 0x1) ^ gfmul8(t[2], 0x2) ^ gfmul8(t[3], 0x0);
		s[i][1] = gfmul8(t[0], 0x0) ^ gfmul8(t[1], 0x3) ^ gfmul8(t[2], 0x1) ^ gfmul8(t[3], 0x2);
		s[i][2] = gfmul8(t[0], 0x2) ^ gfmul8(t[1], 0x0) ^ gfmul8(t[2], 0x3) ^ gfmul8(t[3], 0x1);
		s[i][3] = gfmul8(t[0], 0x1) ^ gfmul8(t[1], 0x2) ^ gfmul8(t[2], 0x0) ^ gfmul8(t[3], 0x3);
	}
}

// Cipher is the main function that encrypts the PlainText.
static void cipherblock(uint8_t state[4][4], const struct AES_ctx *ctx) {
	unsigned numrounds = getNumRounds(ctx->kt);
	const uint32_t *key = ctx->RoundKey;
	while (numrounds--) {
		mix_key((uint32_t*)state, key);
		key += 4;
		sbytes((uint8_t*)state);
		shiftrows(state);
		if (numrounds)
			mixcols(&state[0][0]);
	}

	mix_key((uint32_t*)state, key);
}

static void icipherblock(uint8_t state[4][4], const struct AES_ctx *ctx) {
	unsigned numrounds = getNumRounds(ctx->kt);
	const uint32_t *key = &ctx->RoundKey[numrounds*4];
	mix_key((uint32_t*)state, key);

	for (unsigned i = 0; i < numrounds; i++) {
		if (i)
			imixcols(state);
		ishiftrows(state);
		isbytes((uint8_t*)state);
		key -= 4;
		mix_key((uint32_t*)state, key);
	}
}

void AES_ECB_encrypt(struct AES_ctx *ctx, uint8_t* buf) {
	cipherblock((uint8_t (*)[4])buf, ctx);
}

void AES_ECB_decrypt(struct AES_ctx* ctx, uint8_t* buf) {
	icipherblock((uint8_t (*)[4])buf, ctx);
}

void AES_CFB_encrypt(struct AES_ctx *ctx, uint8_t* buf, int len) {
	while (len > 0) {
		cipherblock((uint8_t (*)[4])ctx->iv, ctx);

		for (int i = 0; i < 16 && i < len; i++)
			ctx->iv[i] = buf[i] = ctx->iv[i] ^ buf[i];

		len -= 16;
		buf += 16;
	}
}

void AES_CFB_decrypt(struct AES_ctx *ctx, uint8_t* buf, int len) {
	while (len > 0) {
		cipherblock((uint8_t (*)[4])ctx->iv, ctx);

		for (int i = 0; i < 16 && i < len; i++) {
			uint8_t c = buf[i];
			buf[i] ^= ctx->iv[i];
			ctx->iv[i] = c;
		}
		len -= 16;
		buf += 16;
	}
}


