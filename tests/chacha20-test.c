int main(int argc, char **argv) {
	int err = 0;
	for (unsigned i = 0; i < sizeof(tests)/sizeof(tests[0]); i++) {
		uint8_t out[249] = {0};
		chacha20_encrypt_decrypt_block((uint32_t*)out, (uint32_t*)out, tests[i].len, &tests[i].key[0], *(uint64_t*)tests[i].nonce);
		err = memcmp(out, tests[i].keystream, tests[i].len);
		// LCOV_EXCL_START
		if (err) {
			printf("Test %d mismatch\n", i);
			printf("Ref: ");
			for (unsigned j = 0; j < tests[i].len; j++)
				printf("%02x", ((uint8_t*)tests[i].keystream)[j]);
			printf("\n");
			printf("Out: ");
			for (unsigned j = 0; j < tests[i].len; j++)
				printf("%02x", (unsigned)out[j]);
			printf("\n");
		}
		if (err)
			return 1;
		// LCOV_EXCL_STOP
	}
	printf("Test OK, no errors!\n");

	return 0;
}


